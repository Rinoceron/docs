function getCookie(name) {
    const escapedName = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const pattern = new RegExp(`(?:^|; )${escapedName}=([^;]*)`);
    const match = document.cookie.match(pattern);
    return match ? decodeURIComponent(match[1]) : null;
}

function detectInitialLang() {
    const cookieLang = getCookie("lang");
    if (cookieLang === "en" || cookieLang === "es") return cookieLang;

    const saved = localStorage.getItem("lang");
    if (saved === "en" || saved === "es") return saved;

    const nav = (navigator.language || "en").toLowerCase();
    return nav.startsWith("es") ? "es" : "en";
}

function setPressedLang(lang) {
    document.querySelectorAll(".lang-btn").forEach((btn) => {
        const isPressed = btn.getAttribute("data-lang") === lang;
        btn.setAttribute("aria-pressed", isPressed ? "true" : "false");
    });
}

function applyI18n(lang) {
    const dict = I18N[lang] || I18N.en;
    const i18nElements = document.querySelectorAll("[data-i18n]");
    let missingKeys = 0;

    document.documentElement.lang = lang;
    i18nElements.forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (!key) return;
        const text = dict[key];
        if (typeof text === "string") {
            el.textContent = text;
        } else {
            missingKeys += 1;
        }
    });

    // #region agent log
    sendAgentDebugLog({
        runId: "run1",
        hypothesisId: "H2",
        location: "frontend/script.js:applyI18n",
        message: "Applied i18n",
        data: {
            lang,
            totalElements: i18nElements.length,
            missingKeys
        }
    });
    // #endregion

    setPressedLang(lang);
}

function setLang(lang, { persist } = { persist: true }) {
    const next = lang === "es" ? "es" : "en";
    if (persist) localStorage.setItem("lang", next);
    applyI18n(next);

    // #region agent log
    sendAgentDebugLog({
        runId: "run1",
        hypothesisId: "H2",
        location: "frontend/script.js:setLang",
        message: "setLang called",
        data: {
            inputLang: lang,
            nextLang: next,
            persist
        }
    });
    // #endregion
}

